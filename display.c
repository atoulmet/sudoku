#include <unistd.h>

void		ft_display(char **tab)
{
	int		x;
	int		y;
	char	c[2];

	y = 0;
	while (y < 9)
	{
		x = 0;
		while (x < 9)
		{
			c[0] = tab[y][x];
			c[1] = ' ';
			write(1, c, x == 8 ? 1 : 2);
			x++;
		}
		write(1, "\n", 1);
		y++;
	}
}
